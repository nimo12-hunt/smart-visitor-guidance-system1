// Search function for departments
export const searchDepartments = (query) => {
  const searchTerm = query.toLowerCase();
  return departmentsData.filter(
    (dept) =>
      dept.name.toLowerCase().includes(searchTerm) ||
      dept.description.toLowerCase().includes(searchTerm) ||
      dept.head?.toLowerCase().includes(searchTerm),
  );
};

// Get departments by sector
export const getDepartmentsBySector = (sectorId) => {
  return departmentsData.filter((dept) => dept.sectorId === sectorId);
};
// Get department by ID - IMPORTANT: Convert id to number
export const getDepartmentById = (id) => {
  return departmentsData.find((dept) => dept.id === parseInt(id));
};
